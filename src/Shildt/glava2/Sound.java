package Shildt.glava2;

/*
Вычисление расстояние до места вспышки молнии, звук от которого
доходит до наблюдателя через 1,2 секунды.
 */
public class Sound {
    public static void main(String[] args) {
        double dist;
        dist = 1100 * 7.2;
        System.out.println(" Расстояние до места вспышки " +
                "состовляет " + dist + " футов");
    }
}
